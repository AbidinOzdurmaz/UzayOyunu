package com.mycompany.uzayoyunu;

//Ateş classımızı oluşturuyoruz constructer ve getter setter methodlarıyla birlikte

public class Ates {
    
     private int x,y;
    
    public Ates(int x,int y){
        this.x=x;
        this.y=y;
       
    }
    
    public int getX(){
        return x;
    }
    public void setX(int x){
        this.x=x;
    }
    public int getY(){
        return y;
    }
    public void setY(int y){
        this.y=y;
    }
    
    
    
    
}
